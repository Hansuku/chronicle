import { createRequire } from "node:module";
import { writeFile } from "node:fs/promises";

const require = createRequire(import.meta.url);
const sourceCities = require("all-the-cities");

const requiredCities = [
  "London",
  "Guangzhou",
  "Rome",
  "Xi'an",
  "Cairo",
  "Istanbul",
  "Baghdad",
  "Delhi",
  "Kyoto",
  "Timbuktu",
  "Mexico City",
  "Cusco",
  "New York City",
  "Buenos Aires",
  "Sydney",
  "Cape Town",
  "Lagos",
  "Nairobi",
  "São Paulo",
  "Lima",
  "Moscow",
  "Paris",
  "Berlin",
  "Lisbon",
  "Athens",
  "Jerusalem",
  "Tehran",
  "Mumbai",
  "Kolkata",
  "Bangkok",
  "Singapore",
  "Jakarta",
  "Beijing",
  "Shanghai",
  "Seoul",
  "Tokyo",
  "Auckland",
];

const normalized = sourceCities
  .filter((city) => {
    const [lng, lat] = city.loc?.coordinates ?? [];
    return Number.isFinite(lat) && Number.isFinite(lng) && city.name && city.country;
  })
  .sort((a, b) => (b.population ?? 0) - (a.population ?? 0));

const selected = new Map();

for (const city of normalized.slice(0, 2200)) {
  selected.set(`${city.name}|${city.country}`, city);
}

for (const name of requiredCities) {
  const candidates = normalized.filter((city) => city.name === name);
  if (candidates[0]) {
    selected.set(`${candidates[0].name}|${candidates[0].country}`, candidates[0]);
  }
}

const output = [...selected.values()]
  .map((city) => ({
    id: String(city.cityId),
    name: city.name,
    country: city.country,
    lat: Number(city.loc.coordinates[1].toFixed(5)),
    lng: Number(city.loc.coordinates[0].toFixed(5)),
    population: city.population ?? 0,
  }))
  .sort((a, b) => b.population - a.population);

const banner = "// Generated by scripts/build-city-index.mjs from all-the-cities (GeoNames-derived, MIT).\n// Do not edit this file by hand.\n";
const moduleSource = `${banner}export const WORLD_CITIES = ${JSON.stringify(output)};\n`;

await writeFile(new URL("../src/data/worldCities.generated.js", import.meta.url), moduleSource);
console.log(`Generated ${output.length} searchable cities.`);
